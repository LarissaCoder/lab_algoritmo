# Declarei as variáveis e solicite a entrada do usuário
ValorA = int(input("Insira aqui o primeiro valor: "))
ValorB = int(input("Insira aqui o segundo valor: "))
ValorC = int(input("Insira aqui o terceiro valor: "))

# Multipliquei os valores
Multiplica = ValorA * ValorB * ValorC

# Calculei a média aritmética
MediaAritmetica = Multiplica / 3

# Apresentei a média
print(f"A média aritmética dos três valores informados é: {MediaAritmetica}")
